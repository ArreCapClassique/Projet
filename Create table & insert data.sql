-- Table Categorie
CREATE TABLE Categorie (
   CategorieId INT PRIMARY KEY,
   NomCat VARCHAR(50) NOT NULL
);


-- Table Produit
CREATE TABLE Produit (
    ProduitId NUMBER PRIMARY KEY,
    NomProd VARCHAR(100) NOT NULL,
    PrixAuKg DECIMAL(10, 2) NULL,
    PrixUnitaire DECIMAL(10, 2),
    Poids DECIMAL(10, 2) NULL,
    Nutriscore CHAR(1) NULL CHECK (Nutriscore IN ('A', 'B', 'C', 'D', 'E')),
    Marque VARCHAR(50) NOT NULL,
    Bio VARCHAR2(5) CHECK (Bio IN ('TRUE', 'FALSE')),
    CategorieId INT NOT NULL,
    CONSTRAINT FK_Categorie FOREIGN KEY (CategorieId) REFERENCES Categorie(CategorieId)
);

-- Table Client
CREATE TABLE Client (
    ClientId NUMBER PRIMARY KEY,
    Nom VARCHAR(100) NOT NULL,
    Prenom VARCHAR(100) NOT NULL,
    Age INT NOT NULL,
    Sexe CHAR(1) NOT NULL CHECK (Sexe IN ('M', 'F', 'O')),
    Adresse VARCHAR(200) NOT NULL
);

-- Table Magasin
CREATE TABLE Magasin (
    MagId NUMBER PRIMARY KEY,
    NomMag VARCHAR(100) NOT NULL,
    AdresseMag VARCHAR(200) NOT NULL,
    CodePostalMag CHAR(5) NOT NULL,
    VilleMag VARCHAR(100) NOT NULL
);
-- Table TypesDeProfil
CREATE TABLE TypesDeProfil (
    ProfilId NUMBER PRIMARY KEY ,
    NomProfil VARCHAR(50) NOT NULL
);

-- Table Commande
CREATE TABLE Commande (
    CommandeId NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    HeureDebut DATE NOT NULL,
    HeureFin DATE NULL,
    HeureFinCommande DATE NULL,
    MagId NUMBER NOT NULL,
    ClientId NUMBER NOT NULL,
    EtatCom VARCHAR(50) NOT NULL CHECK (EtatCom IN ('En preparation', 'En attente de livraison','Finalisee')),
    CONSTRAINT FK_Commande_Magasin FOREIGN KEY (MagId) REFERENCES Magasin(MagId),
    CONSTRAINT FK_Commande_Client FOREIGN KEY (ClientId) REFERENCES Client(ClientId)
);

-- Table Periode
CREATE TABLE Periode (
    PeriodeId NUMBER PRIMARY KEY,
    NomPeriode VARCHAR2(50) NOT NULL,
    DebutPeriode DATE NOT NULL,
    FinPeriode DATE NOT NULL
);


-- Table Etre
CREATE TABLE Etre (
    CategorieId_Principale NUMBER,
    CategorieId_SousCategorie NUMBER,
    PRIMARY KEY (CategorieId_Principale, CategorieId_SousCategorie),
    FOREIGN KEY (CategorieId_Principale) REFERENCES Categorie(CategorieId),
    FOREIGN KEY (CategorieId_SousCategorie) REFERENCES Categorie(CategorieId)
);

-- Table Preferer
CREATE TABLE Preferer (
    ProduitId NUMBER NOT NULL,
    ClientId NUMBER NOT NULL,
    PRIMARY KEY (ProduitId, ClientId),
    CONSTRAINT FK_Preferer_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Preferer_Client FOREIGN KEY (ClientId) REFERENCES Client(ClientId)
);
-- Table Stocker
CREATE TABLE Stocker (
    ProduitId NUMBER NOT NULL,
    MagId NUMBER NOT NULL,
    QteStock NUMBER NOT NULL,
    PRIMARY KEY (ProduitId, MagId),
    CONSTRAINT FK_Stocker_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Stocker_Magasin FOREIGN KEY (MagId) REFERENCES Magasin(MagId)
);

-- Table Appartenir_Type
CREATE TABLE Appartenir_Type (
    ClientId NUMBER NOT NULL,
    ProfilId NUMBER NOT NULL,
    PRIMARY KEY (ClientId, ProfilId),
    CONSTRAINT FK_Appartenir_Type_Client FOREIGN KEY (ClientId) REFERENCES Client(ClientId),
    CONSTRAINT FK_Appartenir_Type_Profil FOREIGN KEY (ProfilId) REFERENCES TypesDeProfil(ProfilId)
);

-- Table Delivrer
CREATE TABLE Delivrer (
    ProduitId NUMBER NOT NULL,
    CommandeId NUMBER NOT NULL,
    QuantiteLivraison NUMBER NOT NULL,
    ModeLivraison VARCHAR(50) NOT NULL CHECK (ModeLivraison IN ('Livraison à domicile', 'Récupérer en magasin','Livraison mixte')) ,
    DateLivraison DATE NOT NULL,
    PRIMARY KEY (ProduitId, CommandeId),
    CONSTRAINT FK_Delivrer_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Delivrer_Commande FOREIGN KEY (CommandeId) REFERENCES Commande(CommandeId)
);

-- Table Repartir
CREATE TABLE Repartir (
    ProduitId NUMBER NOT NULL,
    CommandeId NUMBER NOT NULL,
    MagId NUMBER NOT NULL,
    QteRep NUMBER NOT NULL,
    PRIMARY KEY (ProduitId, CommandeId, MagId),
    CONSTRAINT FK_Repartir_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Repartir_Commande FOREIGN KEY (CommandeId) REFERENCES Commande(CommandeId),
    CONSTRAINT FK_Repartir_Magasin FOREIGN KEY (MagId) REFERENCES Magasin(MagId)
);

-- Table Composer
CREATE TABLE Composer (
    ProduitId NUMBER NOT NULL,
    CommandeId NUMBER NOT NULL,
    QteCom NUMBER NOT NULL,
    PRIMARY KEY (ProduitId, CommandeId),
    CONSTRAINT FK_Composer_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Composer_Commande FOREIGN KEY (CommandeId) REFERENCES Commande(CommandeId)
);

-- Table Correspondre
CREATE TABLE Correspondre (
    ProduitId NUMBER NOT NULL,
    ProfilId NUMBER NOT NULL,
    PRIMARY KEY (ProduitId, ProfilId),
    CONSTRAINT FK_Correspondre_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT FK_Correspondre_Profil FOREIGN KEY (ProfilId) REFERENCES TypesDeProfil(ProfilId)
);

-- Table Lier
CREATE TABLE Recommander (
    ProduitId_est_lié NUMBER NOT NULL,
    ProduitId NUMBER NOT NULL,
    CONSTRAINT fk_produit_est_lié FOREIGN KEY (ProduitId_est_lié) REFERENCES Produit(ProduitId),
    CONSTRAINT fk_produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId),
    CONSTRAINT pk_lier PRIMARY KEY (ProduitId_est_lié, ProduitId),
    CONSTRAINT chk_different CHECK (ProduitId_est_lié <> ProduitId)
);

-- Table Approprier
CREATE TABLE Approprier (
    ProduitId NUMBER NOT NULL,
    PeriodeId NUMBER NOT NULL,
    CONSTRAINT PK_Approprier PRIMARY KEY (ProduitId, PeriodeId),
    CONSTRAINT FK_Approprier_Produit FOREIGN KEY (ProduitId) REFERENCES Produit(ProduitId) ON DELETE CASCADE,
    CONSTRAINT FK_Approprier_Periode FOREIGN KEY (PeriodeId) REFERENCES Periode(PeriodeId) ON DELETE CASCADE
);


